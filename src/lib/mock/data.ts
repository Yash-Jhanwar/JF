export function generatePriceHistory(basePrice: number, days: number = 365): number[] {
  const prices: number[] = [];
  let current = basePrice;
  for (let i = 0; i < days; i++) {
    const change = (Math.random() - 0.5) * basePrice * 0.04;
    current = Math.max(basePrice * 0.7, Math.min(basePrice * 1.3, current + change));
    prices.push(Math.round(current));
  }
  return prices;
}

